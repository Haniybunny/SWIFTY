// Interface Interactive Script Loop Processors
document.addEventListener('DOMContentLoaded', () => {
    const lyricsContent = document.getElementById('lyrics-content');
    const lyricsContainer = document.getElementById('lyrics-window');
    const tracks = document.querySelectorAll('.track-link');

    tracks.forEach(track => {
        track.addEventListener('click', () => {
            // Remove previous active state accents
            tracks.forEach(t => t.classList.remove('accent'));
            track.classList.add('accent');

            const trackId = track.getAttribute('data-track');
            
            if (lyricsContent) {
                if (trackId && albumLyrics[trackId]) {
                    lyricsContent.innerHTML = `<p>${albumLyrics[trackId]}</p>`;
                } else {
                    // Fallback assertion message
                    lyricsContent.innerHTML = `<p>Caught in a wilderness mist...<br><br>Lyrics for <strong>"${track.textContent.trim()}"</strong> are loaded, but data hooks remain unmapped.</p>`;
                }
            }

            // Safe virtual scrolling back to top frame viewport inside device window
            if (lyricsContainer) {
                lyricsContainer.scrollTop = 0;
            }
        });
    });
});